#include <iostream>
#include <vector>
using namespace std;

void findNumber(vector<int> &arr, int a, int b, int c){
  int left =0;
  int right = arr.size()-1;
  bool found = false;
  bool first = true;
  while ( left <= arr.size() && right >= 0){
    int sum = a*arr[left] + b*arr[right];
    if (sum == c){
      found = true;
      if (first) {
        cout << "(" << left << ", " << right << ")";
        first = false;
      }
      else {
        cout << ", (" << left << ", " << right << ")";
      }
    }
    if (sum < c) left++;
    else right--;
  }
  if (!found) cout << "-1" << endl;
}

int main(int argc, char const *argv[]){
  int n;
  cin >> n;
  vector<int> arr(n);
  for (int i=0; i < n; i++){
    cin >> arr[i];
  }
  int a, b, c;
  cout << "a = ";
  cin >> a;
  cout << "b = ";
  cin >> b;
  cout << "c = ";
  cin >> c;
  findNumber(arr, a, b, c);
  return 0;
}
